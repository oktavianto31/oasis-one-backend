import mongoose from "mongoose";

const managementSchema = mongoose.Schema(
  {
    management_id: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Management = mongoose.model("Management", managementSchema);

export default Management;
